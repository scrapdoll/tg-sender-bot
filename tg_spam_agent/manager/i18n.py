def normalize_language(language_code: str | None) -> str:
    return language_code if language_code in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


class Translator:
    def __init__(self, language_code: str | None) -> None:
        self.language_code = normalize_language(language_code)

    def t(self, key: str, **kwargs: object) -> str:
        catalog: Mapping[str, str] = TRANSLATIONS[self.language_code]
        template = catalog.get(key, TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key))
        return template.format(**kwargs) if kwargs else template
